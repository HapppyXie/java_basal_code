package final_char_string关键字;

public class Char和String {
	public static void main(String[] args) {
		System.out.println(3>5);
	     System.out.println(-256>>4);//右移一位相当于除以2
	     System.out.println(-16<<2);//左移一位相当于乘以2
	     
	     char c;//文本型数据,存储单个数字或英文，文本用''括起来
	     c = 'a';
	     char b = 'b';
	     char s = '8';
	     String a = "张三";//字符串类型,存储一串英文和中文，数字
	     
	     String w = "我的狗";
	     String i = "myname";
	     String o = "maybe";
	     String y = "123456789";
	}

}

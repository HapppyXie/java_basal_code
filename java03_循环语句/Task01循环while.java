/*  while 不确定循环次数时

重要的事说三遍：“必须完成课后任务”


*/

//while(循环条件){     循环语句}
public class Task01循环while {
	public static void main(String[] args) {
		int count1 = 1;   //循环的初值，在什么条件下循环
		while(count1 <= 3) {//循环语句 重复做的是=事
			System.out.println("必须完成课后任务");
			count1++;     //打印一次记一次数
		}
		
		int count2 = 1;
		while(count2 <= 3) {
			System.out.println("我爱你，宝！");
			count2++;
		}
		 
	}

}



















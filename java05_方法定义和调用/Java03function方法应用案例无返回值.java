public class Java03function方法应用案例无返回值 {
	public static void main(String[] args) {
		//调用无返回值类型的方法:独立语句调用
		//无返回值的方法需要独立完成一个功能
		welcome();//方法调用
	}
	/*
	 *  功能：欢迎界面
	 *  无返回值
	 * 
	 * */  //方法定义
	public static void welcome() {
		System.out.println("欢迎进入....系统");
		System.out.println("--------------");
	}

}
